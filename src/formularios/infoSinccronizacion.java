    public String depurationProcess( Boolean is_complete ) throws MalformedURLException, IOException{
        try {
            String urlParaVisitar = "http://localhost/" + this.api_local_path + "/rest/crones/depurar_sincronizacion";
            StringBuilder resultado = new StringBuilder();
            URL url = new URL(urlParaVisitar);
            HttpURLConnection conexion = (HttpURLConnection) url.openConnection();
            conexion.setRequestMethod("POST");
            conexion.setRequestProperty("Content-Type", "application/x-www-form-urlencoded");
            conexion.setDoOutput(true);

            // Datos para enviar en el cuerpo del POST
            String urlParameters = "is_complete=" + is_complete; // Ajusta los par√°metros seg√∫n sea necesario

            // Enviar los datos
            DataOutputStream wr = new DataOutputStream(conexion.getOutputStream());
            wr.writeBytes(urlParameters);
            wr.flush();
            wr.close();

            // Leer la respuesta
            BufferedReader rd = new BufferedReader(new InputStreamReader(conexion.getInputStream()));
            String line;
            while ((line = rd.readLine()) != null) {
                resultado.append(line);
            }
            rd.close();

            // Imprimir el resultado
            System.out.println(resultado.toString());
            return resultado.toString();
        } catch (Exception e) {
            e.printStackTrace();
            return "Error";
        }
    }
    
